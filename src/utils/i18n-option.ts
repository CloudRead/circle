export default function (app: App) {
  let i10n: any;
  const lang = app.i18n.getUILanguage();
  const langs = ['en', 'ja', 'ko', 'th', 'zh-CN', 'zh-TW'];
  if (langs.includes(lang)) {
    i10n = i10ns[lang];
  } else {
    const value = lang.split('-').shift() || '';
    i10n = i10ns[langs.includes(value) ? value : 'en'];
  }
  let current;
  const items = [];
  const walker = document.createNodeIterator(document, NodeFilter.SHOW_TEXT);
  while ((current = walker.nextNode())) {
    if (current.nodeValue) {
      const name = current.nodeValue.trim();
      name && items.push(current);
    }
  }
  items.length > 0 &&
    items.forEach((item) => {
      if (item.nodeValue) {
        const name = item.nodeValue.trim();
        if (name) {
          const value = i10n[name] || app.i10n(name);
          if (value) {
            const parent = item.parentElement;
            parent && (parent.innerHTML = value);
          }
        }
      }
    });
}
